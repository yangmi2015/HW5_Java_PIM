    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        int b = jTabbedPane1.getSelectedIndex();
         Calendar a = Calendar.getInstance();
        switch (b) {
            case 0:
               
        try {
            a.setTime(new Date(jTextField9.getText()));
        } catch (java.lang.IllegalArgumentException e) {
            JOptionPane.showMessageDialog(rootPane, "format erro");
            this.dispose();
            return;
        }
                entity = new PIMToDo(a, jTextField8.getText(), flag, priority, jTextField1.getText());
                break;
            case 1:
                entity = new PIMclass.PIMNote(jTextArea1.getText(), flag, priority, jTextField1.getText());
                break;
            case 2:
                entity = new PIMclass.PIMContact(jTextField4.getText(), jTextField5.getText(), jTextField6.getText(), jTextField7.getText(), flag, priority, jTextField1.getText());
                break;
            case 3:
        try {
            a.setTime(new Date(jTextField11.getText()));
        } catch (java.lang.IllegalArgumentException e) {
            JOptionPane.showMessageDialog(rootPane, "format erro");
            this.dispose();
            return;
        }
                entity = new PIMclass.PIMAppointment(a, jTextField10.getText(), flag, priority, jTextField1.getText());
                break;
        }
        try {
            pimfinal.PIMFinal.getFilemanager().add(entity);
        } catch (CustomizedException ex) {
            Logger.getLogger(editFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        pimfinal.PIMFinal.getFram().paint();
        this.dispose();

    }//GEN-LAST:event_jButton1MouseClicked

