    public void paint() {
     
            jTextField1.setText(entity.getOwner());
       
        if (entity.getFlag() == f.Private) {
            jRadioButton1.setSelected(true);
        } else {
            jRadioButton2.setSelected(true);
        }
        switch (entity.getPriority()) {
            case normal:
                jRadioButton4.setSelected(true);
                break;
            case relax:
                jRadioButton3.setSelected(true);
                break;
            case urgent:
                jRadioButton5.setSelected(true);
                break;
        }
        int index=-1;
        if(entity.getClass().getName().equals("PIMclass.PIMToDo"))index=0;
        if(entity.getClass().getName().equals("PIMclass.PIMNote"))index=1;
        if(entity.getClass().getName().equals("PIMclass.PIMContact"))index=2;
        if(entity.getClass().getName().equals("PIMclass.PIMAppointment"))index=3;
        DateFormat format1 = new SimpleDateFormat("yyyy/MM/dd");
        switch(index){
            case 0:
                PIMToDo e=(PIMToDo) entity;
                jTextField8.setText(e.getDescribe());
                jTextField9.setText(format1.format(e.getDate().getTime()));
                jTabbedPane1.setEnabled(false);
                break;
            case 1:
                PIMNote n=(PIMNote) entity;
                jTextArea1.setText(n.getNote());
                jTabbedPane1.setEnabled(false);
                break;
            case 2:
                PIMContact c=(PIMContact) entity;
                jTextField4.setText(c.getFname());
                jTextField5.setText(c.getLname());
                jTextField6.setText(c.getEmail());
                jTextField7.setText(c.getAddress());
                jTabbedPane1.setEnabled(false);
                break;
            case 3:
                PIMAppointment a=(PIMAppointment) entity;
                jTextField10.setText(a.getDescribe());
                jTextField11.setText(format1.format(a.getDate().getTime()));
                jTabbedPane1.setEnabled(false);
                break;
        }
        jTabbedPane1.setSelectedIndex(index);
        jButton3.setVisible(true);
        jButton3.setEnabled(true);
        jButton4.setEnabled(true);
        jButton4.setVisible(true);
        jButton1.setEnabled(false);
        jButton1.setVisible(false);
    }

