        // TODO add your handling code here:
        String text;
        text=jTextField1.getText();
        Pattern pattern = Pattern.compile("^[0-9]{4}(/-|//|/.)[0-9]{0,1}(/-|//|/.)[0-9]{0,1}$");
        Matcher matcher = pattern.matcher(text);
        boolean b=matcher.matches();
