    private void jMenu6MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jMenu6MouseClicked
        // TODO add your handling code here:
        JFileChooser chooser=new JFileChooser();
        chooser.setCurrentDirectory(new File("."));
        int result=chooser.showDialog(jLabel2, "select");
        if(result==JFileChooser.CANCEL_OPTION)return;
        filename=chooser.getSelectedFile().getPath();
        pimfinal.PIMFinal.getFilemanager().setFilename(filename);
        pimfinal.PIMFinal.getCollection().clear();
        ifo_PIM.clear();
        try {
            pimfinal.PIMFinal.setCollection(pimfinal.PIMFinal.getFilemanager().getAll());
        } catch (CustomizedException ex) {
            Logger.getLogger(CalenderFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        paint();
    }//GEN-LAST:event_jMenu6MouseClicked

