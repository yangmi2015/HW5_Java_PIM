    public void paint(){
        this.setIfo_PIM(pimfinal.PIMFinal.getCollection());
        Calendar tem=Calendar.getInstance();
        tem.setTime(date.getTime());
        tem.set(Calendar.DAY_OF_MONTH, 1);
        int[]result=calendar.Calendarinfo.rtinf(tem);
        for(int i=0;i<42;i++){
                        msg[i].removeAll();
                        msg[i].setBackground(Color.white);
                        msg[i].updateUI();
                        if(result[i]==0)continue;
                        if(result[i]==date.get(Calendar.DAY_OF_MONTH)){
                            msg[i].setBackground(Color.red);
                        }
			msg[i].add(new JLabel(Integer.toString(result[i])),BorderLayout.SOUTH);
                        //deal with pim info
                                //不加判断的话后一句有可能会出现nullpoint error
                                if(ifo_PIM==null)continue;
                                String riqi=date.get(Calendar.YEAR)+"/"+(date.get(Calendar.MONTH)+1)+"/"+result[i];
				Date d=new Date(riqi);
                                Calendar na=Calendar.getInstance();
                                na.setTime(d);
                                Collection<PIMEntity> dat;
                                if(!jTextField2.getText().equals("")){
                                    dat=ifo_PIM.getItemsForDate(na,jTextField2.getText());
                                }
                                    else {
                                    dat=ifo_PIM.getItemsForDate(na);
                                }
				if(dat.isEmpty())continue;
				JComboBox<String> faceCombo=new JComboBox<>();
				for(Object a:dat){
					faceCombo.addItem(a.toString());
				}
				msg[i].add(faceCombo,BorderLayout.NORTH);
                                faceCombo.addActionListener(new ActionListener(){

                            @Override
                            public void actionPerformed(ActionEvent e) {
                                jTextArea1.setText(faceCombo.getItemAt(faceCombo.getSelectedIndex()));
                                jTextArea1.repaint();
                               // throw new UnsupportedOperationException("Not supported yet."); //To change body of generated methods, choose Tools | Templates.
                            }
                                    
                                    
                                });
 
                        msg[i].updateUI();
                     
		}
        jTextArea1.setText("");
    }
    
