    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        switch (select){
            case todo:
                if(jTextField1.getText().equals(""))pimcollection=pimfinal.PIMFinal.getCollection().getTodos();
                else pimcollection=pimfinal.PIMFinal.getCollection().getTodos(jTextField1.getText());
                break;
            case note:
                if(jTextField1.getText().equals(""))pimcollection=pimfinal.PIMFinal.getCollection().getNotes();
                else pimcollection=pimfinal.PIMFinal.getCollection().getNotes(jTextField1.getText());
                break;
            case contact:
                if(jTextField1.getText().equals(""))pimcollection=pimfinal.PIMFinal.getCollection().getContacts();
                else pimcollection=pimfinal.PIMFinal.getCollection().getContacts(jTextField1.getText());
                break;
            case appointment:
                if(jTextField1.getText().equals(""))pimcollection=pimfinal.PIMFinal.getCollection().getAppointments();
                else pimcollection=pimfinal.PIMFinal.getCollection().getAppointments(jTextField1.getText());
                break;
            case all:
                if(jTextField1.getText().equals(""))pimcollection=pimfinal.PIMFinal.getCollection().getAll();
                else pimcollection=pimfinal.PIMFinal.getCollection().getAllByOwner(jTextField1.getText());
                break;
        }
    jList1.setModel(new javax.swing.AbstractListModel() {
    PIMCollection in=getPimcollection();
    public int getSize() { return in.size(); }
    public Object getElementAt(int i) { return in.get(i);}
});
    
    }//GEN-LAST:event_jButton1ActionPerformed

